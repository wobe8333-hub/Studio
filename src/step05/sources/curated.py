def fetch_curated_topics(
    category: str,
    limit: int = 20,
    shuffle: bool = True,
) -> Dict[str, Any]:
    """
    카테고리별 큐레이션 에버그린 주제 반환

    Args:
        category: 채널 카테고리
        limit: 반환할 최대 주제 수
        shuffle: True면 무작위 순서로 반환 (매번 다른 주제 조합)

    Returns:
        {
            "topics": List[str],
            "source": "curated",
            "total_pool": int,
            "error": Optional[str]
        }
    """
    pool = _CURATED_TOPICS.get(category)
    if not pool:
        return {
            "topics": [],
            "source": "curated",
            "total_pool": 0,
            "error": f"{category} 카테고리 큐레이션 주제 없음",
        }

    topics = pool.copy()
    if shuffle:
        random.shuffle(topics)

    return {
        "topics": topics[:limit],
        "source": "curated",
        "total_pool": len(pool),
        "error": None,
    }


def get_pool_size(category: str) -> int:
    """카테고리 큐레이션 풀 크기 반환"""
    return len(_CURATED_TOPICS.get(category, []))
